// Endpoint to generate/iterate extension
app.post('/api/generate', async (req, res) => {
  const { prompt, projectId, parentVersion, apiKey: frontendApiKey } = req.body;
  const apiKey = frontendApiKey || process.env.GEMINI_API_KEY;

  if (!prompt) {
    return res.status(400).json({ error: 'Prompt is required' });
  }
  if (!apiKey) {
    return res.status(400).json({ 
      error: 'Gemini API Key is missing. Please configure it in your Settings panel or set GEMINI_API_KEY in the server environment.' 
    });
  }

  const db = readDB();
  let project = null;
  let targetVersion = 1;
  let systemContext = SYSTEM_INSTRUCTION;
  let promptContent = `User requirement: "${prompt}"`;

  // Iteration logic
  if (projectId) {
    project = db.find(p => p.id === projectId);
    if (!project) {
      return res.status(404).json({ error: 'Project not found' });
    }
    const parentVerNum = parseInt(parentVersion) || project.versions.length;
    const parentVer = project.versions.find(v => v.version === parentVerNum);
    
    if (parentVer) {
      targetVersion = project.versions.length + 1;
      
      systemContext += `\n\nThis is an iteration. You are modifying an existing Chrome extension. Below are the files in the current version (Version ${parentVerNum}):`;
      
      parentVer.files.forEach(f => {
        systemContext += `\n\n--- File Name: ${f.name} ---\n${f.content}`;
      });

      promptContent = `The user wants to iterate on the extension. 
Existing Prompt History: ${project.promptHistory ? project.promptHistory.join(' -> ') : ''}
New requirement to apply: "${prompt}"

Modify the existing files or create new ones to achieve this request. Provide the FULL revised project files matching the original JSON format, updating the 'manifest.json' version number if necessary. Maintain existing features unless the user's new request explicitly overrides them.`;
    }
  }

  try {
    const url = `https://generativelanguage.googleapis.com/v1beta/models/gemini-1.5-flash:generateContent?key=${apiKey}`;
    
    const requestBody = {
      contents: [{
        parts: [{ text: promptContent }]
      }],
      systemInstruction: {
        parts: [{ text: systemContext }]
      },
      generationConfig: {
        responseMimeType: "application/json",
        responseSchema: {
          type: "OBJECT",
          properties: {
            name: { type: "STRING" },
            description: { type: "STRING" },
            files: {
              type: "ARRAY",
              items: {
                type: "OBJECT",
                properties: {
                  name: { type: "STRING" },
                  content: { type: "STRING" }
                },
                required: ["name", "content"]
              }
            }
          },
          required: ["name", "description", "files"]
        }
      }
    };

    const response = await fetch(url, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(requestBody)
    });

    if (!response.ok) {
      const errorText = await response.text();
      throw new Error(`Gemini API returned error ${response.status}: ${errorText}`);
    }

    const result = await response.json();
    const rawText = result.candidates?.[0]?.content?.parts?.[0]?.text;
    
    if (!rawText) {
      throw new Error('Gemini API returned an empty response.');
    }

    const cleanedText = cleanJSONResponse(rawText);
    const extensionData = JSON.parse(cleanedText);

    if (!extensionData.files || !Array.isArray(extensionData.files)) {
      throw new Error('LLM output does not contain files array.');
    }

    // Verify manifest.json exists
    const hasManifest = extensionData.files.some(f => f.name.toLowerCase() === 'manifest.json');
    if (!hasManifest) {
      throw new Error('LLM did not generate a manifest.json file.');
    }

    // Create project / version structure
    const newVersion = {
      version: targetVersion,
      prompt: prompt,
      createdAt: new Date().toISOString(),
      files: extensionData.files
    };

    // Temporarily write and validate on physical disk before saving to DB
    const validatedProjectObj = project || { id: crypto.randomUUID() };
    writeToTempAndValidate(validatedProjectObj, newVersion);

    if (project) {
      project.versions.push(newVersion);
      project.promptHistory = project.promptHistory || [];
      project.promptHistory.push(prompt);
      project.name = extensionData.name || project.name;
      project.description = extensionData.description || project.description;
      project.updatedAt = new Date().toISOString();
    } else {
      project = {
        id: validatedProjectObj.id,
        name: extensionData.name || 'Unnamed Extension',
        description: extensionData.description || 'Custom generated Chrome extension.',
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
        promptHistory: [prompt],
        versions: [newVersion]
      };
      db.push(project);
    }

    writeDB(db);
    res.json(project);

  } catch (err) {
    console.error('Generation Error:', err);
    res.status(500).json({ error: err.message || 'Failed to generate extension.' });
  }
});

// Endpoint to save manual user code modifications to db
app.post('/api/projects/:projectId/versions/:version/files', (req, res) => {
  const { projectId, version } = req.params;
  const { name, content } = req.body;

  if (!name || content === undefined) {
    return res.status(400).json({ error: 'File name and content are required' });
  }

  const db = readDB();
  const project = db.find(p => p.id === projectId);
  if (!project) {
    return res.status(404).json({ error: 'Project not found' });
  }

  const verNum = parseInt(version);
  const ver = project.versions.find(v => v.version === verNum);
  if (!ver) {
    return res.status(404).json({ error: 'Version not found' });
  }

  const file = ver.files.find(f => f.name === name);
  if (!file) {
    ver.files.push({ name, content });
  } else {
    file.content = content;
  }

  try {
    // Re-verify local integrity
    writeToTempAndValidate(project, ver);
    project.updatedAt = new Date().toISOString();
    writeDB(db);
    res.json(project);
  } catch (err) {
    res.status(400).json({ error: err.message });
  }
});

// Endpoint to download zip compiled physically on-the-fly from temp directory
app.get('/api/download/:projectId/:version', (req, res) => {
  const { projectId, version } = req.params;
  const db = readDB();
  const project = db.find(p => p.id === projectId);
  
  if (!project) {
    return res.status(404).json({ error: 'Project not found' });
  }

  const verNum = parseInt(version);
  const ver = project.versions.find(v => v.version === verNum);
  
  if (!ver) {
    return res.status(404).json({ error: 'Version not found' });
  }

  try {
    // 1. Physically write files to temp folder and validate structure
    const tempDir = writeToTempAndValidate(project, ver);

    // 2. Set up ZIP streaming
    res.setHeader('Content-Type', 'application/zip');
    res.setHeader('Content-Disposition', `attachment; filename="${project.name.replace(/[^a-zA-Z0-9]/g, '_')}_v${version}.zip"`);

    const archive = archiver('zip', { zlib: { level: 9 } });
    
    archive.on('error', (err) => {
      console.error('Archive error:', err);
      res.status(500).send({ error: err.message });
    });

    archive.pipe(res);

    // 3. Zip files from the physical temp directory
    archive.directory(tempDir, false);
    archive.finalize();

  } catch (err) {
    console.error('Download Error:', err);
    res.status(400).json({ error: err.message });
  }
});

// Endpoint to delete a project and clean up its files
app.delete('/api/projects/:projectId', (req, res) => {
  const { projectId } = req.params;
  const db = readDB();
  const projectIndex = db.findIndex(p => p.id === projectId);
  if (projectIndex === -1) {
    return res.status(404).json({ error: 'Project not found' });
  }
  
  // Remove from db
  db.splice(projectIndex, 1);
  writeDB(db);

  // Remove physical files
  const projectTempDir = path.join(TEMP_BASE_DIR, projectId);
  if (fs.existsSync(projectTempDir)) {
    try {
      fs.rmSync(projectTempDir, { recursive: true, force: true });
    } catch (err) {
      console.error('Error deleting temp directory:', err);
    }
  }
  
  res.json({ success: true });
});

// Endpoint to delete a file from a project version
app.post('/api/projects/:projectId/versions/:version/files/delete', (req, res) => {
  const { projectId, version } = req.params;
  const { name } = req.body;

  if (!name) {
    return res.status(400).json({ error: 'File name is required' });
  }

  const db = readDB();
  const project = db.find(p => p.id === projectId);
  if (!project) {
    return res.status(404).json({ error: 'Project not found' });
  }

  const verNum = parseInt(version);
  const ver = project.versions.find(v => v.version === verNum);
  if (!ver) {
    return res.status(404).json({ error: 'Version not found' });
  }

  const fileIndex = ver.files.findIndex(f => f.name === name);
  if (fileIndex === -1) {
    return res.status(404).json({ error: 'File not found' });
  }

  if (name.toLowerCase() === 'manifest.json') {
    return res.status(400).json({ error: 'Cannot delete manifest.json' });
  }

  ver.files.splice(fileIndex, 1);

  try {
    writeToTempAndValidate(project, ver);
    project.updatedAt = new Date().toISOString();
    writeDB(db);
    res.json(project);
  } catch (err) {
    res.status(400).json({ error: err.message });
  }
});

