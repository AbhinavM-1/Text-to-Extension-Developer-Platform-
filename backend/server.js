// Helper to read DB
const readDB = () => {
  try {
    if (!fs.existsSync(DB_PATH)) {
      fs.writeFileSync(DB_PATH, JSON.stringify([]));
    }
    const data = fs.readFileSync(DB_PATH, 'utf8');
    return JSON.parse(data);
  } catch (err) {
    console.error('Error reading DB:', err);
    return [];
  }
};

// Helper to write DB
const writeDB = (data) => {
  try {
    fs.writeFileSync(DB_PATH, JSON.stringify(data, null, 2));
  } catch (err) {
    console.error('Error writing DB:', err);
  }
};

// Helper to write to local temp directory and validate Chrome V3 Manifest schema
const writeToTempAndValidate = (project, versionObj) => {
  const tempDir = path.join(TEMP_BASE_DIR, project.id, `v${versionObj.version}`);
  
  // Clean & Ensure directory exists
  if (fs.existsSync(tempDir)) {
    fs.rmSync(tempDir, { recursive: true, force: true });
  }
  fs.mkdirSync(tempDir, { recursive: true });

  // Write all files physically to disk
  versionObj.files.forEach(f => {
    const filePath = path.join(tempDir, f.name);
    // Ensure any nested directory generated by the LLM exists
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    fs.writeFileSync(filePath, f.content, 'utf8');
  });

  // Validate manifest.json
  const manifestFile = versionObj.files.find(f => f.name.toLowerCase() === 'manifest.json');
  if (!manifestFile) {
    throw new Error('Validation Error: manifest.json is missing from the extension folder.');
  }

  try {
    const manifestJson = JSON.parse(manifestFile.content);
    if (parseInt(manifestJson.manifest_version) !== 3) {
      throw new Error('Validation Error: The manifest.json must use manifest_version 3 to be a valid modern extension.');
    }
    if (!manifestJson.name) {
      throw new Error('Validation Error: manifest.json is missing the mandatory "name" property.');
    }
    if (!manifestJson.version) {
      throw new Error('Validation Error: manifest.json is missing the mandatory "version" property.');
    }

    // Helper to check if file exists in list
    const fileExists = (name) => {
      if (!name) return true;
      const cleanName = name.replace(/^\.?\//, '');
      return versionObj.files.some(f => {
        const fClean = f.name.replace(/^\.?\//, '');
        return fClean.toLowerCase() === cleanName.toLowerCase();
      });
    };

    // 1. Check content scripts js/css references
    if (manifestJson.content_scripts && Array.isArray(manifestJson.content_scripts)) {
      manifestJson.content_scripts.forEach((cs, index) => {
        if (cs.js && Array.isArray(cs.js)) {
          cs.js.forEach(jsFile => {
            if (!fileExists(jsFile)) {
              throw new Error(`Validation Error: "content_scripts[${index}]" references missing JavaScript file "${jsFile}".`);
            }
          });
        }
        if (cs.css && Array.isArray(cs.css)) {
          cs.css.forEach(cssFile => {
            if (!fileExists(cssFile)) {
              throw new Error(`Validation Error: "content_scripts[${index}]" references missing CSS file "${cssFile}".`);
            }
          });
        }
      });
    }

    // 2. Check background service worker / script references
    if (manifestJson.background) {
      if (manifestJson.background.service_worker) {
        const swFile = manifestJson.background.service_worker;
        if (!fileExists(swFile)) {
          throw new Error(`Validation Error: "background.service_worker" references missing file "${swFile}".`);
        }
      }
      if (manifestJson.background.scripts && Array.isArray(manifestJson.background.scripts)) {
        manifestJson.background.scripts.forEach(scriptFile => {
          if (!fileExists(scriptFile)) {
            throw new Error(`Validation Error: "background.scripts" references missing file "${scriptFile}".`);
          }
        });
      }
    }

    // 3. Check action default popup
    if (manifestJson.action && manifestJson.action.default_popup) {
      const popupFile = manifestJson.action.default_popup;
      if (!fileExists(popupFile)) {
        throw new Error(`Validation Error: "action.default_popup" references missing file "${popupFile}".`);
      }
    }

    // 4. Check options pages references
    if (manifestJson.options_page && !fileExists(manifestJson.options_page)) {
      throw new Error(`Validation Error: "options_page" references missing file "${manifestJson.options_page}".`);
    }
    if (manifestJson.options_ui && manifestJson.options_ui.page) {
      const optUiPage = manifestJson.options_ui.page;
      if (!fileExists(optUiPage)) {
        throw new Error(`Validation Error: "options_ui.page" references missing file "${optUiPage}".`);
      }
    }

    // 5. Check chrome_url_overrides references
    if (manifestJson.chrome_url_overrides) {
      const overrides = manifestJson.chrome_url_overrides;
      if (overrides.newtab && !fileExists(overrides.newtab)) {
        throw new Error(`Validation Error: "chrome_url_overrides.newtab" references missing file "${overrides.newtab}".`);
      }
      if (overrides.bookmarks && !fileExists(overrides.bookmarks)) {
        throw new Error(`Validation Error: "chrome_url_overrides.bookmarks" references missing file "${overrides.bookmarks}".`);
      }
      if (overrides.history && !fileExists(overrides.history)) {
        throw new Error(`Validation Error: "chrome_url_overrides.history" references missing file "${overrides.history}".`);
      }
    }
  } catch (err) {
    if (err.message.includes('JSON')) {
      throw new Error(`Validation Error: manifest.json has syntax errors and is not valid JSON. Detail: ${err.message}`);
    }
    throw err;
  }

  return tempDir;
};

