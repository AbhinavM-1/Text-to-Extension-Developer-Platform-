app.post('/api/generate', async (req, res) => {
  const { prompt, projectId, parentVersion, apiKey: frontendApiKey, aiProvider: frontendAiProvider } = req.body;
  const apiKey = frontendApiKey || process.env.OPENAI_API_KEY;
  const aiProvider = frontendAiProvider || 'openai';

  if (!prompt) {
    return res.status(400).json({ error: 'Prompt is required' });
  }
  if (!apiKey) {
    return res.status(400).json({ 
      error: 'OpenAI API Key is missing. Please configure it in your Settings panel or set OPENAI_API_KEY in the server environment.' 
    });
  }

  const db = readDB();
  let project = null;
  let targetVersion = 1;
  let systemContext = SYSTEM_INSTRUCTION;
  let promptContent = `User requirement: "${prompt}"`;

  // Iteration logic
  if (projectId) {
    project = db.find(p => p.id === projectId);
    if (!project) {
      return res.status(404).json({ error: 'Project not found' });
    }
    const parentVerNum = parseInt(parentVersion) || project.versions.length;
    const parentVer = project.versions.find(v => v.version === parentVerNum);
    
    if (parentVer) {
      targetVersion = project.versions.length + 1;
      
      systemContext += `\n\nThis is an iteration. You are modifying an existing Chrome extension. Below are the files in the current version (Version ${parentVerNum}):`;
      
      parentVer.files.forEach(f => {
        systemContext += `\n\n--- File Name: ${f.name} ---\n${f.content}`;
      });

      promptContent = `The user wants to iterate on the extension. 
Existing Prompt History: ${project.promptHistory ? project.promptHistory.join(' -> ') : ''}
New requirement to apply: "${prompt}"

Modify the existing files or create new ones to achieve this request. Provide the FULL revised project files matching the original JSON format, updating the 'manifest.json' version number if necessary. Maintain existing features unless the user's new request explicitly overrides them.`;
    }
  }

  try {
    let rawText = '';
    try {
      if (aiProvider === 'gemini') {
        const response = await fetch(`https://generativelanguage.googleapis.com/v1beta/models/gemini-2.0-flash:generateContent?key=${apiKey}`, {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({
            system_instruction: { parts: [{ text: systemContext }] },
            contents: [{ parts: [{ text: promptContent }] }],
            generationConfig: { response_mime_type: 'application/json', temperature: 0.7 }
          })
        });
        if (!response.ok) {
          const errBody = await response.text();
          console.error('Gemini error body:', errBody);
          throw new Error(`Gemini API Error ${response.status}`);
        }
        const result = await response.json();
        rawText = result.candidates?.[0]?.content?.parts?.[0]?.text;
      } else if (aiProvider === 'anthropic') {
        const response = await fetch('https://api.anthropic.com/v1/messages', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            'x-api-key': apiKey,
            'anthropic-version': '2023-06-01'
          },
          body: JSON.stringify({
            model: 'claude-3-5-sonnet-20240620',
            max_tokens: 4096,
            system: systemContext,
            messages: [{ role: 'user', content: promptContent }]
          })
        });
        if (!response.ok) {
          const errBody = await response.text();
          console.error('Anthropic error body:', errBody);
          throw new Error(`Anthropic API Error ${response.status}`);
        }
        const result = await response.json();
        rawText = result.content?.[0]?.text;
      } else {
        // Default to OpenAI
        const response = await fetch('https://api.openai.com/v1/chat/completions', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            'Authorization': `Bearer ${apiKey}`
          },
          body: JSON.stringify({
            model: 'gpt-4o-mini',
            messages: [
              { role: 'system', content: systemContext },
              { role: 'user', content: promptContent }
            ],
            response_format: { type: 'json_object' },
            temperature: 0.7
          })
        });
        if (!response.ok) {
          const errBody = await response.text();
          console.error('OpenAI error body:', errBody);
          throw new Error(`OpenAI API Error ${response.status}`);
        }
        const result = await response.json();
        rawText = result.choices?.[0]?.message?.content;
      }
    } catch (apiErr) {
      console.error('API call failed:', apiErr.message);
      throw apiErr;
    }
    
    if (!rawText) {
      throw new Error('API returned an empty response and mock fallback failed.');
    }

    const cleanedText = cleanJSONResponse(rawText);
    const extensionData = JSON.parse(cleanedText);

    if (!extensionData.files || !Array.isArray(extensionData.files)) {
      throw new Error('LLM output does not contain files array.');
    }

    // Verify manifest.json exists
    const hasManifest = extensionData.files.some(f => f.name.toLowerCase() === 'manifest.json');
    if (!hasManifest) {
      throw new Error('LLM did not generate a manifest.json file.');
    }

    // Create project / version structure
    const newVersion = {
      version: targetVersion,
      prompt: prompt,
      createdAt: new Date().toISOString(),
      files: extensionData.files
    };

    // Temporarily write and validate on physical disk before saving to DB
    const validatedProjectObj = project || { id: crypto.randomUUID() };
    writeToTempAndValidate(validatedProjectObj, newVersion);

    if (project) {
      project.versions.push(newVersion);
      project.promptHistory = project.promptHistory || [];
      project.promptHistory.push(prompt);
      project.name = extensionData.name || project.name;
      project.description = extensionData.description || project.description;
      project.updatedAt = new Date().toISOString();
    } else {
      project = {
        id: validatedProjectObj.id,
        name: extensionData.name || 'Unnamed Extension',
        description: extensionData.description || 'Custom generated Chrome extension.',
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
        promptHistory: [prompt],
        versions: [newVersion]
      };
      db.push(project);
    }

    writeDB(db);
    res.json(project);

  } catch (err) {
    console.error('Generation Error:', err);
    res.status(500).json({ error: err.message || 'Failed to generate extension.' });
  }
});

