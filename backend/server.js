app.get('/api/download/:projectId/:version', (req, res) => {
  const { projectId, version } = req.params;
  const db = readDB();
  const project = db.find(p => p.id === projectId);
  
  if (!project) {
    return res.status(404).json({ error: 'Project not found' });
  }

  const verNum = parseInt(version);
  const ver = project.versions.find(v => v.version === verNum);
  
  if (!ver) {
    return res.status(404).json({ error: 'Version not found' });
  }

  try {
    // 1. Physically write files to temp folder and validate structure
    const tempDir = writeToTempAndValidate(project, ver);

    // 2. Set up ZIP streaming
    res.setHeader('Content-Type', 'application/zip');
    res.setHeader('Content-Disposition', `attachment; filename="${project.name.replace(/[^a-zA-Z0-9]/g, '_')}_v${version}.zip"`);

    const archive = archiver('zip', { zlib: { level: 9 } });
    
    archive.on('error', (err) => {
      console.error('Archive error:', err);
      res.status(500).send({ error: err.message });
    });

    archive.pipe(res);

    // 3. Zip files from the physical temp directory
    archive.directory(tempDir, false);
    archive.finalize();

  } catch (err) {
    console.error('Download Error:', err);
    res.status(400).json({ error: err.message });
  }
});

